C                .      .    .                                       .
C SUBPROGRAM:    RDAFIL           READING THE DATA FIELD
C   PRGMMR: LILLY            ORG: W/NMC412   DATE: 93-05-11
C
C ABSTRACT: PLACES DATA FIELD INTO TEMPORARY STORAGE, AND
C   WRITES STRIP TITLE (IE; MAP BACKGROUND, DATE/TIME ETC).
C
C PROGRAM HISTORY LOG:
C   YY-MM-DD  ORIGINAL AUTHOR  UNKNOWN
C   89-04-25  STEVE LILLY  DOCUMENT
C   93-05-11  LILLY CONVERT SUB. TO FORTRAN 77
C
C USAGE:    CALL RDAFIL(INFILE,NOLVSR,LVLSAL,KRUN,IOPCLO,LCKOUT,
C           &           IOPENQ,IERROR)
C
C   INPUT ARGUMENT LIST:
C     INFILE   - INPUT FILE NUMBER SUCH AS 19 FOR ADPUPA
C     NOLVSR   - NO. OF MANDATORY LEVELS REQUESTED
C     LVLSAL   - ARRAY OF ALL LEVELS REQUESTED (BY INDEX NO.)
C     KRUN     - FOR SETTING OPTIONS BY RUN TYPE, SUCH AS =11 TROPIC
C     LCKOUT   - TRUE. IF CHECKOUT RUN FOR MORE PRINT. LOGICAL  
