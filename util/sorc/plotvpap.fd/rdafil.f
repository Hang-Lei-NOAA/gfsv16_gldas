C
C   OUTPUT ARGUMENT LIST:
C     IOPCLO   - TO OPEN/CLOSE GRANPA FILE
C              - =1, CLOSE GRANPA AFTER PROCESSING THIS INP FILE
C              - =2, OPEN GRANPA FILE THEN PROCESS THIS INP FILE
C              - =4, CLOSE GRANPA IMMEDIATELY
C     IERROR   - =0, NORMAL EXIT
C              - =1, RETURN FROM REDADP WITH ERROR
C     COMMON /BOBIN / LOCT(256),IDTBL(1539)
C     COMMON /TIMES / NANJI(12)
C
C REMARKS:
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN 77
C   MACHINE:  NAS
C
C$$$
C
      COMMON /BOBIN / LOCT(256),IDTBL(1539)
      COMMON /TIMES / NANJI(12)
C
      CHARACTER*8 GRANPA
      DATA        GRANPA            /'GRANPA  '/
C
      INTEGER    LVLSAL(11)
      INTEGER    MESSG(10)
C
      LOGICAL    LCKOUT
C
      RETURN
      END
