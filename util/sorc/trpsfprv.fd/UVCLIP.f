      SUBROUTINE UVCLIP(U,V,A,B,IMAX,JMAX,UM,UA,JFLD)
C     ...THIS SUBROUTINE WILL SCALE AND FLIP SIDEWAYS AND CLIP OFF2 FLDS00138400
      DIMENSION U(117,51),V(117,51)
      DIMENSION A(IMAX,JMAX)
      DIMENSION B(IMAX,JMAX)
C     ...FIRST WE SCALE                                                 00138800
      DO 195 I=1,117
      DO 195 J=1,51
      U(I,J)=U(I,J)*UM+UA
      V(I,J)=V(I,J)*UM+UA
  195 CONTINUE
C     ...FLIP V-FIELD SIDEWAYS AND CLIP AND MOVE INTO IMAX,JMAX ARRAY   00139400
      DO 320 I=1,117
      JNEW=I+1
      DO 310 J=1,IMAX
      JR=51+1-J
      A(J,JNEW)=V(I,JR)
  310 CONTINUE
  320 CONTINUE
      DO 330 I=1,IMAX
      A(I,1)=A(I,117)
      A(I,119)=A(I,3)
  330 CONTINUE
C     ...THEN OVERWRITE THE ORIGINAL V WITH FLIPPED V                   00140600
      DO 350 J=1,JMAX
      DO 340 I=1,IMAX
      B(I,J)=A(I,J)
  340 CONTINUE
  350 CONTINUE
      IF(JFLD.EQ.1) RETURN
C     ...TO FLIP U FIELD SIDEWAYS AND CLIP INTO IMAX,JMAX ARRAY         00141300
      DO 370 I=1,117
      JNEW=I+1
      DO 360 J=1,IMAX
      JR=51+1-J
      A(J,JNEW)=U(I,JR)
  360 CONTINUE
  370 CONTINUE
      DO 380 I=1,IMAX
      A(I,1)=A(I,117)
      A(I,119)=A(I,3)
  380 CONTINUE
      RETURN
      END
